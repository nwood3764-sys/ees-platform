/** Return the schema array for a section_type_value, or null if no schema. */
export function getSectionConfigSchema(sectionTypeValue) {
  return SECTION_CONFIG_SCHEMAS[sectionTypeValue] || null
}

/**
 * Build a default prts_config object for a newly-created section of the given
 * type. Unknown types return {}.
 */
export function buildDefaultConfig(sectionTypeValue) {
  const schema = SECTION_CONFIG_SCHEMAS[sectionTypeValue]
  if (!schema) return {}
  const out = {}
  for (const f of schema) {
    if (f.type === 'info') continue
    if (f.default !== undefined) out[f.key] = f.default
  }
  return out
}
